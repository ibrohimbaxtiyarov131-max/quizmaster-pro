// =============================================================================
//
// Структура вопроса:
//   id        — уникальный идентификатор (число)
//   type      — тип вопроса:
//                 "single"   — одиночный выбор (радиокнопки)
//                 "multiple" — множественный выбор (чекбоксы)
//                 "text"     — текстовый ответ (поле ввода)
//   question  — текст вопроса
//   options   — варианты ответов (для single и multiple)
//   correct   — правильный ответ:
//                 для "single"   — строка с правильным вариантом
//                 для "multiple" — массив строк с правильными вариантами
//                 для "text"     — строка или массив строк (допустимые ответы)
//   explanation — (необязательно) пояснение к правильному ответу
//
// =============================================================================

export interface Question {
  id: number
  type: 'single' | 'multiple' | 'text'
  question: string
  options?: string[]
  correct: string | string[]
  explanation?: string
}

export interface QuizConfig {
  title: string
  description: string
  timeLimit?: number // в минутах (0 = без ограничений)
  passingScore: number // процент для сдачи
}

